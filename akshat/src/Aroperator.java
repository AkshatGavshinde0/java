
public class Aroperator {
    public static void main(String[] args) {
        int x=20;
        int y=10;


        // addition operator
        System.out.println("x + y = " + (x + y));

        // subtraction operator
        System.out.println("x - y = " + (x - y));

        // multiplication operator
        System.out.println("x * y = " + (x * y));

        // division operator
        System.out.println("x/ y = " + (x / y));

        // modulo operator
        System.out.println("x % y = " + (x % y));




    }
}
